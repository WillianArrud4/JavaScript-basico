
/*

adição: +
subtração: -
multiplicação: *
incremento: ++
decremento: --

atribuição += -=


*/

// Adição
let a = 10
let b = 5
let c = a + b
console.log(a + b)

// Subtração
let d = 10
let e = 5
let f = d . e
console.log(f)

// Multiplicação
let g = 10
let h = 5
let i = g * h
console.log(i)

// Divisão
let j = 10
let k = 5
let l = j / k 
console.log(l)

// Incremento
let m = 10
m++
console.log(m)

// Decremento
let n = 10 
n-- 
console.log(n)

// Atribuição +
 let o = 12
o += 25  // o = 0 + 25   
console.log(o)

// Atribuição -
let p = 12
p -= 25 // p = p + 25
console.log(p)

